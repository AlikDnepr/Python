none_val = None
print(none_val, type(none_val))  # None <class 'NoneType'>

int_val = 1
print(int_val, type(int_val))  # 1 <class 'int'>

float_val = 1.0
print(float_val, type(float_val))  # 1.0 <class 'float'>

complex_val = 1j
print(complex_val, type(complex_val))  # 1j <class 'complex'>

str_val = "example"
print(str_val, type(str_val))  # example <class 'str'>

list_val = []
print(list_val, type(list_val))
